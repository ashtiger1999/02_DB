-- 2.
SELECT TS.STUDENT_NAME 이름, TS.STUDENT_SSN 주민번호
FROM TB_STUDENT ts 
WHERE ts.ABSENCE_YN = 'Y'
ORDER BY 주민번호 DESC;

-- 3.
SELECT TS.STUDENT_NAME 학생이름, TS.STUDENT_NO 학번, TS.STUDENT_ADDRESS 주소지, TS.ENTRANCE_DATE
FROM TB_STUDENT ts 
WHERE (TS.STUDENT_ADDRESS LIKE '강원도%'
OR TS.STUDENT_ADDRESS LIKE '경기도%')
AND TS.ENTRANCE_DATE<'2000-01-01';

