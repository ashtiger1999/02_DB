FROM TB_DEPARTMENT td, TB_STUDENT ts, TB_PROFESSOR tp
WHERE td.DEPARTMENT_NO = ts.DEPARTMENT_NO
AND ts.COACH_PROFESSOR_NO = tp.PROFESSOR_NO
AND ts.STUDENT_NO = 'A313047';

-- 12.
