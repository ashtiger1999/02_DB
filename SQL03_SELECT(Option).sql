FROM TB_STUDENT ts, TB_GRADE tg
WHERE ts.STUDENT_NO = tg.STUDENT_NO
AND term_no LIKE '2007%'
AND tg.CLASS_NO = (SELECT class_no FROM TB_CLASS tc WHERE class_name = '인간관계론');

