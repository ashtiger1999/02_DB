-- 10.
SELECT student_no, student_name, round(avg(point),1)
FROM TB_STUDENT
JOIN TB_GRADE using(STUDENT_NO)
where TB_STUDENT.DEPARTMENT_NO = (SELECT department_no FROM TB_DEPARTMENT td WHERE td.DEPARTMENT_NAME ='음악학과')
GROUP BY STUDENT_NO, STUDENT_NAME
ORDER BY student_no;

