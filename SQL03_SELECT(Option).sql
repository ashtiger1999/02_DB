SELECT*FROM TB_CLASS tc ;
SELECT*FROM TB_DEPARTMENT td ;
SELECT TC.CLASS_NAME 강의명, TD.DEPARTMENT_NAME 학과
FROM TB_CLASS tc 
JOIN TB_DEPARTMENT td USING (DEPARTMENT_NO);

-- 8.
SELECT * FROM TB_PROFESSOR tp ;
SELECT * FROM TB_CLASS_PROFESSOR tcp ;
SELECT TC.CLASS_NAME 강의명, TP.PROFESSOR_NAME 교수
FROM TB_CLASS tc, TB_CLASS_PROFESSOR tcp, TB_PROFESSOR tp
WHERE TC.CLASS_NO  = TCP.CLASS_NO
AND TCP.PROFESSOR_NO = TP.PROFESSOR_NO;

